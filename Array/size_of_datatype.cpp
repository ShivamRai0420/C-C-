#include<iostream>
using namespace std;
int main()
{
    int a=5;
    cout<<"size of int="<<sizeof(int)<<endl;
    cout<<"value of a is ="<<a<<endl;
    bool b=true;
    cout<<"value of bool="<<b<<endl;
    float f=1.23;
    cout<<"value of float="<<f<<endl;
    char ch='s';
    cout<<"value of ch="<<ch<<endl;
    cout<<"size of char="<<sizeof(char);
}